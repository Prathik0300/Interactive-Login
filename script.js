

function openEye(){
    var open = document.getElementById("open");
    var close = document.getElementById("close");
    open.style.display = "";
    close.style.display = "none";
    
}


function closeEye(){
    var open = document.getElementById("open");
    var close = document.getElementById("close");
    open.style.display = "none";
    close.style.display = "";
}